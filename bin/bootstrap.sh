#!/usr/bin/env bash
# bootstrap.sh — fork zava-* repos into your org and rewrite refs
# Usage: ./bin/bootstrap.sh --org=YOUR_ORG [--source-org=DevExpGbb]
set -euo pipefail

ORG=""
SOURCE_ORG="DevExpGbb"
SKIP_TEMPLATE=true   # trainees use "Use this template" instead

for a in "$@"; do
  case "$a" in
    --org=*) ORG="${a#--org=}" ;;
    --source-org=*) SOURCE_ORG="${a#--source-org=}" ;;
    --include-template) SKIP_TEMPLATE=false ;;
    *) echo "unknown arg: $a"; exit 2 ;;
  esac
done
[[ -z "$ORG" ]] && { echo "usage: $0 --org=<github-org> [--source-org=DevExpGbb]"; exit 2; }

WORK="${WORK:-/tmp/zava-bootstrap-$ORG}"
mkdir -p "$WORK" && cd "$WORK"

REPOS=(zava-agent-config zava-storefront poisoned-tracing-skill)
$SKIP_TEMPLATE || REPOS+=(zava-skills-workshop-template)

echo "=== bootstrap → $ORG (source=$SOURCE_ORG) ==="

# 1. Fork each repo (idempotent: gh repo fork is no-op if exists)
for r in "${REPOS[@]}"; do
  echo
  echo "--- $r ---"
  if gh api "repos/$ORG/$r" >/dev/null 2>&1; then
    echo "  ✅ $ORG/$r already exists, skipping fork"
  else
    echo "  → forking $SOURCE_ORG/$r → $ORG/$r"
    gh repo fork "$SOURCE_ORG/$r" --org "$ORG" --default-branch-only --clone=false
    sleep 5  # let GitHub finish the fork
  fi
done

# 2. Rewrite refs in each fork: $SOURCE_ORG/ → $ORG/
for r in "${REPOS[@]}"; do
  echo
  echo "--- rewriting refs in $ORG/$r ---"
  rm -rf "$WORK/$r"
  gh repo clone "$ORG/$r" "$WORK/$r" -- --quiet
  cd "$WORK/$r"

  # Find files containing the source org slug in apm.yml or workflow files
  hits=$(grep -rl "$SOURCE_ORG/" --include="*.yml" --include="*.yaml" --include="*.md" --include="*.json" . 2>/dev/null | grep -v "^./.git/" || true)
  if [[ -z "$hits" ]]; then
    echo "  ✅ no $SOURCE_ORG/ refs found, skipping"
    cd "$WORK"
    continue
  fi
  echo "  → rewriting in:"
  echo "$hits" | sed 's/^/      /'
  echo "$hits" | xargs sed -i.bak "s|$SOURCE_ORG/|$ORG/|g"
  find . -name "*.bak" -delete

  if git diff --quiet; then
    echo "  ✅ no diff after rewrite (already clean)"
  else
    git -c user.name="Zava Workshop Kit" -c user.email="zava-kit@example.com" \
      commit -am "chore: rewrite source-org refs $SOURCE_ORG → $ORG

Generated by zava-workshop-kit/bin/bootstrap.sh.
Repinning marketplace + workflow imports to your org."
    echo "  ✅ committed rewrites (push deferred until lockfile regen)"
  fi

  # Regenerate APM lockfile if apm.yml exists. Cross-org tag tarballs hash
  # differently even at the same version (each release.yml run produces a
  # fresh tarball), so the inherited lockfile would trip apm audit's
  # supply-chain check. apm CLI is required only when consumer repos pin
  # marketplace plugins.
  if [[ -f apm.yml ]] && command -v apm >/dev/null 2>&1; then
    echo "  → regenerating apm.lock.yaml against $ORG content"
    if apm install --update >/dev/null 2>&1; then
      if ! git diff --quiet apm.lock.yaml 2>/dev/null; then
        git -c user.name="Zava Workshop Kit" -c user.email="zava-kit@example.com" \
          commit -m "chore(apm): regenerate lockfile against $ORG plugins" apm.lock.yaml
        echo "  ✅ lockfile regenerated"
      else
        echo "  ✅ lockfile already current"
      fi
    else
      echo "  ⚠️  apm install --update failed — push and regenerate manually"
    fi
  elif [[ -f apm.yml ]]; then
    echo "  ⚠️  apm.yml present but 'apm' CLI not on PATH — skipping lockfile regen"
    echo "     install apm and rerun: cd $WORK/$r && apm install --update && git commit -am 'chore(apm): lockfile' && git push"
  fi

  if ! git diff --quiet origin/HEAD HEAD 2>/dev/null; then
    git push origin HEAD
    echo "  ✅ pushed"
  fi
  cd "$WORK"
done

# 3. Org .github repo + apm-policy.yml
echo
echo "--- org policy: $ORG/.github ---"
if ! gh api "repos/$ORG/.github" >/dev/null 2>&1; then
  echo "  → creating $ORG/.github"
  gh repo create "$ORG/.github" --public --description "Org defaults for $ORG" --add-readme
  sleep 3
fi
rm -rf "$WORK/.github"
gh repo clone "$ORG/.github" "$WORK/.github" -- --quiet
cd "$WORK/.github"
mkdir -p .
KIT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cp "$KIT_DIR/templates/apm-policy.yml" ./apm-policy.yml
if git diff --quiet && [[ ! -n "$(git status --porcelain)" ]]; then
  echo "  ✅ apm-policy.yml already in place"
else
  git add apm-policy.yml
  git -c user.name="Zava Workshop Kit" -c user.email="zava-kit@example.com" \
    commit -m "chore: org-level apm-policy.yml from zava-workshop-kit"
  git push origin HEAD || true
  echo "  ✅ apm-policy.yml committed"
fi
cd "$WORK"

# 4. Trigger marketplace release on the fork (so your org has its own v5.0.1 release)
echo
echo "--- triggering release on $ORG/zava-agent-config ---"
LATEST_TAG=$(gh api "repos/$SOURCE_ORG/zava-agent-config/releases/latest" --jq .tag_name 2>/dev/null || echo "")
if [[ -z "$LATEST_TAG" ]]; then
  echo "  ⚠️  could not read latest tag from source — skipping release trigger"
elif gh api "repos/$ORG/zava-agent-config/releases/tags/$LATEST_TAG" >/dev/null 2>&1; then
  echo "  ✅ release $LATEST_TAG already published in $ORG"
else
  cd "$WORK/zava-agent-config"
  if ! git tag | grep -q "^$LATEST_TAG$"; then
    echo "  → fetching tags"
    git fetch --tags origin || true
  fi
  if git tag | grep -q "^$LATEST_TAG$"; then
    git push origin "$LATEST_TAG" 2>/dev/null || echo "  (tag push: may already exist)"
    echo "  → triggering release.yml workflow"
    gh workflow run release.yml --repo "$ORG/zava-agent-config" --ref "$LATEST_TAG" 2>/dev/null \
      || echo "  ⚠️  workflow_dispatch failed — push tag manually or trigger from UI"
  else
    echo "  ⚠️  tag $LATEST_TAG not in local clone — fork may not have inherited tags"
    echo "      run: git fetch upstream --tags && git push origin --tags"
  fi
  cd "$WORK"
fi

echo
echo "=== ✅ bootstrap complete ==="
echo "   workspace: $WORK"
echo "   next: ./bin/smoke.sh --org=$ORG"
